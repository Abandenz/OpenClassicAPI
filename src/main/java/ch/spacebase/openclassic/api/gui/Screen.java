package ch.spacebase.openclassic.api.gui;

/**
 * Represents a screen.
 */
public interface Screen {
	
	/**
	 * Gets the width of the screen.
	 * @return The screen's width.
	 */
	public int getWidth();
	
	/**
	 * Gets the height of the screen.
	 * @return The screen's height.
	 */
	public int getHeight();
	
}
